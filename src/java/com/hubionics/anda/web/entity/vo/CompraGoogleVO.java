public class CompraGoogleVO {


    private String googleIdInAppPurchase;
    private Integer idUsuario;
    private String googleOrderID;


    public CompraGoogleVO() {
    }

    public String getGoogleIdInAppPurchase() {
        return googleIdInAppPurchase;
    }

    public void setGoogleIdInAppPurchase(String idPaquete) {
        this.googleIdInAppPurchase = idPaquete;
    }

    public Integer getIdUsuario() {
        return idUsuario;
    }

    public void setIdUsuario(Integer idUsuario) {
        this.idUsuario = idUsuario;
    }

    public String getGoogleOrderID() {
        return googleOrderID;
    }

    public void setGoogleOrderID(String googleOrderID) {
        this.googleOrderID = googleOrderID;
    }
}
